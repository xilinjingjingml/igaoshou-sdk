const {ccclass, property} = cc._decorator;

@ccclass
export default class Start extends cc.Component {

    @property(cc.Label)
    label: cc.Label = null;

    @property
    text: string = 'hello';

    // LIFE-CYCLE CALLBACKS:

    // onLoad () {}

    start () {
        cc.assetManager.loadBundle("igaoshou", (err, res)=>{
            res.loadScene("main", (err, sce)=>{
                cc.director.runScene(sce)
            })
        })
    }

    // update (dt) {}
}
